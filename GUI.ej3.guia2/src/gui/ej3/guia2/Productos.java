    private void jbAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarActionPerformed
        // TODO add your handling code here:
        try {

            if (jtNombre.getText().isEmpty() || jtPrecio.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "No debe haber campos vacios");
            } else {
                String nombre = jtNombre.getText();
                String categoria = jcCategoria.getSelectedItem().toString();
                double precio = Double.parseDouble(jtPrecio.getText());
                Productoss pro = new Productoss(nombre, categoria, precio);
                cargarDatos(pro);
            }
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Solo se admiten numero separados con punto");
        }
    }//GEN-LAST:event_jbAgregarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here: //BOTON ELIMINAR
        int filasS = jtTabla.getSelectedRow();  //Devuelve la fila seleccionada
        if (filasS != -1){
            modelo.removeRow(filasS);
        }else{
            JOptionPane.showMessageDialog(this, "Usted debe seleccionar una fila para borrar");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

