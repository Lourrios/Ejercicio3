private void armarCabecera(){ //Armar cabecera es para colocar los titulos de la tabla
        modelo.addColumn("Nombre");
        modelo.addColumn("Categoria");
        modelo.addColumn("Precio");
        jtTabla.setModel(modelo);
    }

private void cargarCombo(){ // Cargar combo es para cargar los item adentro 
    
    jcCategoria.addItem("Electronica");
    jcCategoria.addItem("Ropa");
    jcCategoria.addItem("Alimentos");
    jcCategoria.addItem("Limpieza");
    
}

private void cargarDatos(Productoss producto){
    modelo.addRow(new Object[]{producto.getNombre(),producto.getCategoria(),producto.getPrecio()});
    
}
